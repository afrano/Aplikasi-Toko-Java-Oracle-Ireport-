    private void TOMBOLSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TOMBOLSIMPANActionPerformed
        // TODO add your handling code here:
        if (TEKS_KODE.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Silahkan isi Kode Kategori");}
        else if (TEKS_NAMAKATEGORI.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Silahkan isi Nama Kategori");    
        }
        else {
            try {
                String kode=TEKS_KODE.getText();
                String kategori = TEKS_NAMAKATEGORI.getText();
                KategoriBrng kat=new KategoriBrng();
                kat.setKode(kode);
                kat.setKategori(kategori);
                KategoriBrng kk=Kontrol_Kategori.getKoneksi().carikodeKategori(kode);
                
                if (kode.equals(kk.getKode())&&! TOMBOLSIMPAN.getText().equals("UPDATE")){
                    JOptionPane.showMessageDialog(null,"Maaf kode "+kode+" sudah digunakan ");
                } else {
                    Kontrol_Kategori konkat=Kontrol_Kategori.getKoneksi();
                if (TOMBOLSIMPAN.getText().equals("UPDATE")) {
                    konkat.updateKategori(kat,label_kode.getText());
                    bersihTeks();
                JOptionPane.showMessageDialog(null,"Terupdate");
                }else {
                konkat.tambahKategori(kat);
                bersihTeks();
                JOptionPane.showMessageDialog(null,"Tersimpan");
                }
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Gagal menyimpan karena "+ex.getMessage());
            }
    }//GEN-LAST:event_TOMBOLSIMPANActionPerformed
    }
        private void TABEL_LISTKATEGORIMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TABEL_LISTKATEGORIMouseClicked
        int row=TABEL_LISTKATEGORI.getSelectedRow();
        String kode=TABEL_LISTKATEGORI.getValueAt(row, 1).toString();
        String kategori=TABEL_LISTKATEGORI.getValueAt(row, 2).toString();
        label_kode.setText(kode);
        TEKS_KODE.setText(kode);
        TEKS_NAMAKATEGORI.setText(kategori);
        TOMBOLSIMPAN.setText("UPDATE");
        //to string berguna untuk tipe data apapun akan di String kan
    }//GEN-LAST:event_TABEL_LISTKATEGORIMouseClicked

    private void TOMBOLHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TOMBOLHAPUSActionPerformed
        if (TEKS_KODE.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Maaf, Kode barang yang ingin dihapus tidak ada ");
        }
        else    {
       try{
           Kontrol_Kategori konkat=Kontrol_Kategori.getKoneksi();
           konkat.hapusKategori(TEKS_KODE.getText());
           bersihTeks();
           JOptionPane.showMessageDialog(null,"Terhapus");
       } catch (SQLException ex){
           JOptionPane.showMessageDialog(null,"Gagal hapus karena "+ex.getMessage());
       }
    }//GEN-LAST:event_TOMBOLHAPUSActionPerformed
    }
