    public void bersihTeks(){
        label_kode.setText("");
        TEKS_KODE.setText("");
        TEKS_NAMAKATEGORI.setText("");
        tampilKategori();
        TOMBOLSIMPAN.setText("SIMPAN");
        
    }
    public void tampilKategori(){
        try {
            List<KategoriBrng> kat=Kontrol_Kategori.getKoneksi().tampilKategori();
            TabelModel_Kategori model=new TabelModel_Kategori(kat);
            TABEL_LISTKATEGORI.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(GUI_Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
